# These operators are used to test if a sequence is presented in an object
a = ["Apple", "Banana", "Cherry"]

# 1) value in objectName
# Returns True if a sequence with the specified value is present in the object
print("variableName in objectName")
print("Apple" in a)
print("Durian" in a)

# 1) value not in objectName
# Returns True if a sequence with the specified value is not present in the object
print("variableName not in objectName")
print("Apple" not in a)
print("Durian" not in a)