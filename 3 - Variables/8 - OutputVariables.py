# The print() function is used to output variables
a = "a"
print(a)

# To output multiple variables, you can either use commas (,)
b = "b"
c = "c"
d = "d"
print(b, c, d) # The strings here are not joined together as there is a space between the commas

# You can also use the + operator
e = "e"
f = "f"
g = "g"
print(e + f + g) # The strings here are joined together as there is no space separating them

# When using the + operator, combining strings and numbers together produces an error
# Hence, it is recommended to use commas when combining strings