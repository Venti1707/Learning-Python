# int()
# 1. Constructs an integer number from an integer literal.
# 2. A float literal (by removing all decimals).
# 3. A string literal (providing the string represents a whole number).
a = int(1)   # a will be 1.
b = int(2.7) # b will be 2.
c = int("3") # c will be 3.

print(a)
print(type(a))
print(b)
print(type(b))
print(c)
print(type(c))