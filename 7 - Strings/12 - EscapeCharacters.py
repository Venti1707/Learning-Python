# Escape characters are a backslash (\) followed by the character you want to insert
# For instance, to add double quotes to a string which is defined with double quotes as well
print("TARDIS stands for \"Time And Relative Dimension In Space\"")

# The following is a list of escape characters in Python
# \' - Single Quote
# \\ - Backslash
# \n - New Line
# \r - Carriage Return
# \t - Tab
# \b - Backspace
# \f - Form Feed
# \ooo - Octal value
# \xhh - Hex value