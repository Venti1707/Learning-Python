# Upper case
# Done with the upper() method
a = "Hello world!"
print(a.upper())

# Lower case
# Done with the lower() method
print(a.lower())

# Removing whitespace
# Done with the strip() method
b = "Hello world! "
print(b.strip())

# Replacing strings with other strings
# Done with the replace() method
print(a.replace("H", "J"))

# Splitting strings into a list
# Done with the split() method
print(a.split(" "))