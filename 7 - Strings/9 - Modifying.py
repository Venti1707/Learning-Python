a = "Hello world!"
# Upper case
# Done with the upper() method
print("Upper case")
print(a.upper())

# Lower case
# Done with the lower() method
print("Lower case")
print(a.lower())

# Removing whitespace
# Done with the strip() method
b = "Hello world! "
print("Removing whitespace")
print(b.strip())

# Replacing strings with other strings
# Done with the replace() method
print("Replacing strings with other strings")
print(a.replace("H", "J"))

# Splitting strings into a list
# Done with the split() method
print("Splitting into a list")
print(a.split(" "))