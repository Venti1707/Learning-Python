# These operators are used with numeric values to perform common mathematical operations
a = 22
b = 7

# 1) Addition (+)
print("Addition")
print(a + b)

# 2) Subtraction (-)
print("Subtraction")
print(a - b)

# 3) Multiplication (*)
print("Multiplication")
print(a * b)

# 4) Division (/)
print("Division")
print(a / b)

# 5) Modulus (%) - Gives the remainder when a is divided by b
print("Modulus")
print(a % b)

# 6) Exponentiation (**)
print("Exponentiation")
print(a ** b)

# 7) Floor division (//) - Rounds the result down to the nearest whole number when a is divided by b
print("Floor division")
print(a // b)