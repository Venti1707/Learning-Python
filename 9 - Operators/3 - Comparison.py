# These operators are used to compare numerical values
# They return Boolean values
a = 22
b = 7

# 1) variableName == value
# Returns True when variableName and value are the same
print("variableName == value")
print(a == b)

# 2) variableName != value
# Returns True when variableName and value are not the same
print("variableName != value")
print(a != b)

# 3) variableName > value
# Returns True when variableName is greater than value
print("variableName > value")
print(a > b)

# 4) variableName < value
# Returns True when variableName is lesser than value
print("variableName < value")
print(a < b)

# 5) variableName >= value
# Returns True when variableName is greater than or equal to value
print("variableName >= value")
print(a >= b)

# 6) variableName <= value
# Returns True when variableName is lesser than or equal to value
print("variableName <= value")
print(a <= b)